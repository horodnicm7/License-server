public class Rectangle {
    public int topLeftLine;
    public int topLeftCol;
    public int bottomRightLine;
    public int bottomRightCol;

    public Rectangle(int topLeftLine, int topLeftCol, int bottomRightLine, int bottomRightCol) {
        this.topLeftLine = topLeftLine;
        this.topLeftCol = topLeftCol;
        this.bottomRightLine = bottomRightLine;
        this.bottomRightCol = bottomRightCol;
    }
}